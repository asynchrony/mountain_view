var MountainViewPseudoGenerator;

MountainViewPseudoGenerator = (function() {
  function MountainViewPseudoGenerator() {
    var i, idx, idxs, j, len, len1, pseudos, ref, ref1, replaceRule, rule, stylesheet;
    pseudos = /(\:hover|\:disabled|\:active|\:visited|\:focus|\:checked)/g;
    try {
      ref = document.styleSheets;
      for (i = 0, len = ref.length; i < len; i++) {
        stylesheet = ref[i];
        idxs = [];
        ref1 = stylesheet.cssRules;
        for (idx = j = 0, len1 = ref1.length; j < len1; idx = ++j) {
          rule = ref1[idx];
          if ((rule.type === CSSRule.STYLE_RULE) && pseudos.test(rule.selectorText)) {
            replaceRule = function(matched, stuff) {
              return ".pseudo-class-" + matched.replace(':', '');
            };
            this.insertRule(rule.cssText.replace(pseudos, replaceRule));
          }
        }
      }
    } catch (_error) {}
  }

  MountainViewPseudoGenerator.prototype.insertRule = function(rule) {
    var headEl, styleEl;
    headEl = document.getElementsByTagName('head')[0];
    styleEl = document.createElement('style');
    styleEl.type = 'text/css';
    if (styleEl.styleSheet) {
      styleEl.styleSheet.cssText = rule;
    } else {
      styleEl.appendChild(document.createTextNode(rule));
    }
    console.log(rule)
    return headEl.appendChild(styleEl);
  };

  return MountainViewPseudoGenerator;

})();

new MountainViewPseudoGenerator;

